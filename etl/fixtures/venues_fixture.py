def get_venues() -> list[dict[str, Any]]:
    """Return the 25 Festival d'Avignon venue fixtures."""
    return VENUES


def save_venues(output_dir: Path) -> None:
    """Write venue fixtures to JSON and CSV in *output_dir*."""
    output_dir.mkdir(parents=True, exist_ok=True)

    json_path = output_dir / "venues_fixture.json"
    json_path.write_text(
        json.dumps(VENUES, ensure_ascii=False, indent=2),
        encoding="utf-8",
    )

    csv_path = output_dir / "venues_fixture.csv"
    with csv_path.open("w", encoding="utf-8", newline="") as fh:
        writer = csv.DictWriter(fh, fieldnames=list(VENUES[0].keys()))
        writer.writeheader()
        writer.writerows(VENUES)
